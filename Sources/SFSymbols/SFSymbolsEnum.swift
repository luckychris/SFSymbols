struct SFImage : View {
    
    private var isResizable = false
    
    var systemName: SFSymbolsEnum
    
    public init(systemName: SFSymbolsEnum, isResizable : Bool = false) {
        self.isResizable = isResizable
        self.systemName = systemName
    }
    
    public func resizable() -> SFImage {
        return SFImage(systemName: systemName, isResizable: true)
    }
    
    var body: some View {
        let image = Image(systemName: self.systemName.rawValue)
        return isResizable ? image.resizable() : image
    }
}

